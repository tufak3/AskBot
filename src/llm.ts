import Groq from "groq-sdk";
import { config } from "./config.js";
import type { ChatMessage } from "./conversation.js";

/**
 * Обёртка над Groq API (LLM-ответы бота).
 * Этап 5: ответы на основе базы знаний + контекст + сигнал о переводе на оператора.
 */

const groq = new Groq({ apiKey: config.groqApiKey });

/** Бесплатная production-модель Groq с хорошим балансом качества/скорости. */
const MODEL = "llama-3.3-70b-versatile";

/** Скрытая метка: модель добавляет её, если нужен перевод на оператора. */
const OPERATOR_TAG = "[[NEED_OPERATOR]]";

/** Запасной ответ, если модель вернула пустой результат. */
const FALLBACK_REPLY =
  "Извините, не получилось сформировать ответ. Попробуйте, пожалуйста, переформулировать вопрос.";

export interface LlmReply {
  /** Текст для клиента (метка уже удалена). */
  text: string;
  /** Нужно ли запустить сбор контакта и уведомить оператора. */
  needsOperator: boolean;
}

/** Собирает системный промпт с базой знаний и строгими правилами против выдумок. */
function buildSystemPrompt(knowledge: string): string {
  return [
    "Ты — консультант поддержки интернет-магазина кроссовок и уличной обуви «STEP UP».",
    "Общайся на русском языке, на «вы», дружелюбно и по-деловому.",
    "Отвечай коротко и по существу — обычно 2–3 предложения.",
    "Учитывай предыдущие сообщения диалога, чтобы понимать контекст вопроса.",
    "",
    "ВАЖНЫЕ ПРАВИЛА:",
    "1. Отвечай ТОЛЬКО на основе приведённой ниже базы знаний.",
    "2. Если точного ответа в базе нет — НЕ придумывай и не угадывай.",
    "   Честно скажи, что по этому вопросу нет точной информации и лучше уточнить у менеджера.",
    "   НЕ проси у клиента контакт сам — об этом позаботится система.",
    "3. Не выдумывай цены, размеры, сроки и условия, которых нет в базе.",
    "4. По умолчанию метку НЕ добавляй. Если в базе знаний есть ответ на вопрос —",
    `   НИКОГДА не добавляй метку ${OPERATOR_TAG}, просто отвечай.`,
    `5. Добавь метку ${OPERATOR_TAG} в самом конце ответа на отдельной строке`,
    "   ТОЛЬКО в двух случаях: (а) в базе реально НЕТ нужной информации для ответа;",
    "   (б) клиент прямо просит связать его с живым человеком/менеджером.",
    "   Метку не упоминай в тексте и ничего про неё не объясняй.",
    "   Приветствие, благодарность и общие фразы — это НЕ повод для метки.",
    "",
    "=== БАЗА ЗНАНИЙ ===",
    knowledge || "(база знаний пуста)",
    "=== КОНЕЦ БАЗЫ ЗНАНИЙ ===",
  ].join("\n");
}

/**
 * Генерирует ответ с учётом базы знаний и истории диалога.
 * Возвращает текст (без служебной метки) и флаг необходимости оператора.
 */
export async function generateReply(
  userText: string,
  knowledge: string,
  history: ChatMessage[],
): Promise<LlmReply> {
  const completion = await groq.chat.completions.create({
    model: MODEL,
    messages: [
      { role: "system", content: buildSystemPrompt(knowledge) },
      ...history,
      { role: "user", content: userText },
    ],
    temperature: 0.3,
    max_tokens: 500,
  });

  const raw = completion.choices[0]?.message?.content ?? "";
  const needsOperator = raw.includes(OPERATOR_TAG);
  const text = raw.split(OPERATOR_TAG).join("").trim() || FALLBACK_REPLY;

  return { text, needsOperator };
}
